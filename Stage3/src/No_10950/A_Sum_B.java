package No_10950;

import java.util.Scanner;

public class A_Sum_B {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);

		System.out.print("");
		int t = sc.nextInt();

		for (int i = 1; i <= t; i++) {
			System.out.print("");
			int a = sc.nextInt();
			int b = sc.nextInt();
			System.out.println(a + b);
		}
	}
}